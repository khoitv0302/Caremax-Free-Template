function showSubMenuGlass() {
    var subMenu = document.getElementById("hideOrShowGlass");
    var rotateDropDown = document.getElementById("rotateAngleDownGlass");
    if (subMenu.style.display === "block") {
      subMenu.style.display = "none";
      rotateDropDown.style.transform = "rotate(0)";
    } else {
      subMenu.style.display = "block";
      rotateDropDown.style.transform = "rotate(180deg)";
    }

  }
